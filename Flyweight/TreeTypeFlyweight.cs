using System;
namespace Flyweight
{
	public class TreeTypeFlyweight
	{
		private string name;

		private string color;

		public TreeTypeFlyweight(string name, string color)
        {
			this.name = name;
			this.color = color;
        }
		public void Draw(int height)
        {
			Console.WriteLine(String.Format("Drawing {0} {1} tree. Height: {2}", color, name, height));
        }

		public string GetName()
        {
			return name;
        }

		public string GetColor()
		{
			return color;
		}

	}
	
}
